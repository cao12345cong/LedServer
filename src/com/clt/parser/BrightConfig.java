package com.clt.parser;
/**
 * 自动亮度调节曲线
 */
public class BrightConfig
{
    private int isAuto;

    private String brightTune;

    public int getIsAuto()
    {
        return isAuto;
    }

    public void setIsAuto(int isAuto)
    {
        this.isAuto = isAuto;
    }

    public String getBrightTune()
    {
        return brightTune;
    }

    public void setBrightTune(String brightTune)
    {
        this.brightTune = brightTune;
    }

    
}
